    private void defaultBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_defaultBtnActionPerformed
        // TODO add your handling code here:
        int selectedrow = jTable.getSelectedRow();
        String newAccountName = accountNamejTextField.getText();
        String newPassword = passwordjTextField.getText();

        if (newAccountName.equals("") || newPassword.equals("")) {
            JOptionPane.showMessageDialog(null, "Please fill in both blanks!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (selectedrow >= 0) {
            UserAccount ua = (UserAccount) jTable.getValueAt(selectedrow, 0);
            if (!newAccountName.equals(ua.getUsername())) {
                if (!system.isFeasible(newAccountName)) {
                    JOptionPane.showMessageDialog(null, "Duplicated account name! Please pick another one!", "Warning", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }
            ua.setUsername(newAccountName);
            ua.setPassword(newPassword);
            JOptionPane.showMessageDialog(null, "ID: " + ua.getPersonnel().getId() + "\nNew Account Name: " + newAccountName + "\nNew Pwd: " + newPassword, "Information", JOptionPane.INFORMATION_MESSAGE);
            populateTable();
        } else {
            JOptionPane.showMessageDialog(null, "Please select an account from the table first!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_defaultBtnActionPerformed

