    public void populateTable() {

        DefaultTableModel dtm = (DefaultTableModel) jTable.getModel();
        dtm.setRowCount(0);

        for (Organization o : system.getOrganizationDirectory().getOrganizationList()) {
            for (UserAccount ua : o.getUserAccountDirectory().getUserAccountList()) {
                Object[] row = new Object[6];
                row[0] = ua;
                row[1] = ua.getPassword();
                row[2] = ua.getRole().toString().substring(14);
                row[3] = ua.getPersonnel().getId();
                row[4] = ua.getPersonnel().getName();
                row[5] = "N/A";
                dtm.addRow(row);
            }
        }
        for (Club c : system.getClubDirectory().getClubList()) {
            for (Organization o : c.getOrganizationDirectory().getOrganizationList()) {
                for (UserAccount ua : o.getUserAccountDirectory().getUserAccountList()) {
                    Object[] row = new Object[6];
                    row[0] = ua;
                    row[1] = ua.getPassword();
                    row[2] = ua.getRole().toString().substring(14);
                    row[3] = ua.getPersonnel().getId();
                    row[4] = ua.getPersonnel().getName();
                    row[5] = c.getClubName();
                    dtm.addRow(row);
                }
            }
        }

    }

