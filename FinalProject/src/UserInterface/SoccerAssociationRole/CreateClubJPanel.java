    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        // TODO add your handling code here:
        String clubName = clubNamejTextField.getText();
        String clubAddress = clubAddressjTextField.getText();
        String clubManagerName = clubManagerNamejTextField.getText();
        String clubManagerAccountName = clubManagerAccountNamejTextField.getText();
        String clubManagerPassword = clubManagerPasswordjTextField.getText();

        if (clubName.equals("") || clubAddress.equals("") || clubManagerName.equals("") || clubManagerAccountName.equals("") || clubManagerPassword.equals("")) {
            JOptionPane.showMessageDialog(null, "Please fill in all blanks!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        for (Club c : clubDirectory.getClubList()) {
            if (c.getClubName().equals(clubName)) {
                JOptionPane.showMessageDialog(null, "Duplicated club name! Please pick another one!", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }

        if (system.isFeasible(clubManagerAccountName)) {
            clubDirectory.addClub(clubName, clubAddress, clubManagerName, clubManagerAccountName, clubManagerPassword);
            JOptionPane.showMessageDialog(null, "Club " + clubName + " successfully added!", "Successful", JOptionPane.INFORMATION_MESSAGE);
            clubNamejTextField.setText("");
            clubAddressjTextField.setText("");
            clubManagerNamejTextField.setText("");
            clubManagerAccountNamejTextField.setText("");
            clubManagerPasswordjTextField.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Duplicated manager account name! Please pick another one!", "Warning", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_createBtnActionPerformed

