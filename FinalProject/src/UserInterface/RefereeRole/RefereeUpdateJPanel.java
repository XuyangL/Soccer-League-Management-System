    private void setDefault() {
        refereeNamejTextField.setText(userAccount.getPersonnel().getName());
        refereeAccountNamejTextField.setText(userAccount.getUsername());
        refereePasswordjTextField.setText(userAccount.getPassword());
    }
    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        // TODO add your handling code here:
        String refereeName = refereeNamejTextField.getText();
        String refereeAccountName = refereeAccountNamejTextField.getText();
        String refereePassword = refereePasswordjTextField.getText();
        if (refereeName.equals("") || refereeAccountName.equals("") || refereePassword.equals("")) {
            JOptionPane.showMessageDialog(null, "Please fill in all blanks!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (!refereeAccountName.equals(userAccount.getUsername())) {
            if (!system.isFeasible(refereeAccountName)) {
                setDefault();
                JOptionPane.showMessageDialog(null, "Duplicated account name! Please pick another one!", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }
        userAccount.getPersonnel().setName(refereeName);
        userAccount.setUsername(refereeAccountName);
        userAccount.setPassword(refereePassword);
        JOptionPane.showMessageDialog(null, "Referee " + refereeName + " Successfully updated!", "Successful", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_createBtnActionPerformed

